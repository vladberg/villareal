                            <p style="text-align: justify;text-indent: 0.5cm;font-size: 16px">La cirugía de la nariz (rinoplastia) es una operación utilizada para producir cambios en el aspecto, estructura y función de la nariz. La rinoplastia puede reducir o aumentar el tamaño de la nariz, cambiar la forma de la punta, regularizar el dorso, estrechar la anchura de las alas, o cambiar el ángulo entre la nariz y el labio superior. Esta operación puede ayudar a corregir defectos de nacimiento, lesiones nasales, y algunos problemas respiratorios. </p>
