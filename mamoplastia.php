<p style="text-align: justify;text-indent: 0.5cm;font-size: 16px">El procedimiento tiene una duración aproximada de 90 minutos y se lleva a cabo, por lo general, de forma ambulatoria y se realiza bajo anestesia general. La cirugía consiste en la implantación de prótesis, ya sea detrás del tejido mamario o debajo de los músculos del tórax. Las incisiones se realizan de forma que las cicatrices resulten lo menos visibles que sea posible, habitualmente alrededor de la parte inferior de la areola, por debajo de la mama o en la axila. El método de implantación y la posición de la prótesis dependerán de sus preferencias, su anatomía y la recomendación del Cirujano Plástico. </p>
