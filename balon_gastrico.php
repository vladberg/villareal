                                El balón intragástrico es un dispositivo ideado para facilitar la pérdida de peso en pacientes con obesidad. Se trata de una esfera de silicona que una vez introducida en el estómago, bajo control endoscópico, se rellena con 600 cc de agua y se deja flotando libremente en la cavidad gástrica. El balón, al ocupar parcialmente el estómago y dificultar su vaciado, produce una disminución de la sensación de hambre y la aparición de saciedad precoz al comer. Con estos dos efectos es más fácil seguir una dieta adecuada y poder cambiar los hábitos dietéticos y de ejercicio necesarios para, no sólo conseguir una pérdida de peso, sino también mantenerla los meses siguientes.</p>
<p style="text-align: justify;text-indent: 0.5cm;font-size: 16px">La colocación del balón está indicada en pacientes obesos de 18 a 60 años, con un índice de masa corporal entre 30 y 40 kg (obesidad pre mórbida), que normalmente corresponde a un sobrepeso de 15-25 kg. Excepcionalmente se coloca el balón en casos de obesidad mórbida para disminuir el riesgo quirúrgico o en presencia de contraindicaciones para la cirugía, y en casos de sobrepeso leve hasta la corrección de este.
El balón por sí solo no produce una pérdida de peso: hay que seguir controles por el médico especialista que indicará y supervisará la dieta, la cual podrá seguir sin dificultad al no existir la sensación de hambre, ni la aparición de la ansiedad y demás efectos indeseables que normalmente se asocian al seguimiento de dietas de adelgazamiento.
El balón se lleva durante 6 meses, después hay que retirarlo. Durante los 2-3 primeros días es normal tener náuseas, vómitos y molestias en epigastrio pues el estómago intenta liberarse del balón. Pasados estos primeros días no suele haber más efectos indeseables.

